"""

Escreva uma função chamada "concatenar" que recebe duas strings como parâmetros:
"prefixo" e "sufixo". A função deve retornar a concatenação das duas strings.

"""

def concatenar(prefixo: str, sufixo: str) -> str:
    """
    Essa função faz ua concatenação dos parâmetros
    """
    return prefixo + sufixo

