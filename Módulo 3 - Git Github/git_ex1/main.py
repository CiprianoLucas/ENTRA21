def adicao(a: int, b: int) -> int:
    """
    soma o primeiro número com o segundo número
    args:
        a: primeiro número
        b: segundo número
    return: soma
    """
    return a + b


def divisao(a: int, b: int) -> float:
    """
    divide o primeiro número com o segundo número
    args:
        a: primeiro número
        b: segundo número
    return: divisão
    """
    return a / b


def subtracao(a: int, b: int) -> int:
    """
    Subtrae o primeiro número com o segundo número
    args:
        a: primeiro número
        b: segundo número
    return: subtração
    """
    return a - b


def multiplicacao(a: int, b: int) -> int:
    """
    multiplica o primeiro número com o segundo número
    args:
        a: primeiro número
        b: segundo número
    return: multiplicação
    """
    return a * b
